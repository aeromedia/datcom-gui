    //Called upon if the user selects the "Browse" function
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //start the browseBox if it is not already open
        if (browseBox == null) {
            mainFrame = dd2jsbApp.getApplication().getMainFrame();
            browseBox = new dd2jsbBrowser(mainFrame);
            browseBox.setLocationRelativeTo(mainFrame);
        }
        dd2jsbApp.getApplication().show(browseBox);

        //grab the selected dd2jsbOp file's path and store it it to "filePath"
        getFilePath();
        jTextField1.setText(filePath);
    }//GEN-LAST:event_jButton3ActionPerformed

    //Called upon if the user selects to "Import" the selected dd2jsbOp file
    //from the browser box
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //grab the necessary values from the text boxes
        workingPath = jTextField2.getText();
        caseID = jTextField3.getText();

        //flash the mainPanel OFF
        mainPanel.setVisible(false);

        //store mainClass
        mainClass = this;

        //create a new dd2jsbOp file review class, and pass the necessary
        //information to it
        reviewFor006 = new dd2jsbOp(filePath, workingPath, caseID, mainClass, mainPanel);

        //set the review panel to the main jFrame
        setComponent(reviewFor006);

        //flash the review dd2jsbOp panel ON
        reviewFor006.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed
