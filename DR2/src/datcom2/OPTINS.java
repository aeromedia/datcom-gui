    public void getInput(){
        Utilities util = new Utilities();
        SREF = "SREF=" + util.validateInput(jTextField1.getText());
        CBARR = "CBARR=" + util.validateInput(jTextField2.getText());
        BLREF = "BLREF=" + util.validateInput(jTextField3.getText());

        OPTINS = new ArrayList<String>();
        OPTINS.add(SREF);
        OPTINS.add(CBARR);
        OPTINS.add(BLREF);

        OPTINSvalues = new ArrayList<String>();
        OPTINSvalues.add(jTextField1.getText());
        OPTINSvalues.add(jTextField2.getText());
        OPTINSvalues.add(jTextField3.getText());

        OPTINSfiltered = new ArrayList<String>();

        //filter out the null values from the array
        for(int i = 0; i < OPTINS.size(); i ++){
            if (OPTINSvalues.get(i).equals("")){}
            else{
                System.out.println(OPTINS.get(i));
                OPTINSfiltered.add(OPTINS.get(i));
            }
        }
        System.out.println("End of output");
    }

