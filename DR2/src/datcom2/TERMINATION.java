    public void getInput(){
        //if the input is ON then store the corresponding TERMINATINO
        //command.
        TERMINATION = new ArrayList<String>();
        if (jComboBox1.getSelectedItem().equals("ON"))
        {
            BUILD = "BUILD";
            TERMINATION.add(BUILD);
        } 
        if (jComboBox2.getSelectedItem().equals("ON"))
        {
            PLOT = "PLOT";
            TERMINATION.add(PLOT);
        }
    }

