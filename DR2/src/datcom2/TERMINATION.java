    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //If NEXT button is pressed collect and pass along all the input
        getInput();
        terminationPanel = this;

        //make the for005 file
        for005 = makeFor005.createFor005(caseID, FLTCON, OPTINS, SYNTHS, BODY, WGPLNF, HTPLNF, VTPLNF, TERMINATION);
        makeFor005.writeFile(workingPath, "for005.dat", for005);
        for005Panel = new for005(workingPath + "/for005.dat", workingPath,caseID, terminationPanel, mainClass);

        //switch panels to for005
        setVisible(false);
        mainClass.setComponent(for005Panel);
        for005Panel.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

