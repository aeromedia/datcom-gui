    private String[] getArrayFromString (String tempField){
        String tempArrayString = "";
        int arrayCounter = 0;
        int countARRAYnumbersINfield = 0;

        char[] tempFieldArray = tempField.toCharArray();

        for(int z = 0; z < tempFieldArray.length;z++)
        {
            if(tempFieldArray[z] == ','){
                countARRAYnumbersINfield ++;
            }
        }

        //System.out.println("found this many numbers: " + countARRAYnumbersINfield);

        String[] ARRAY = new String[countARRAYnumbersINfield];

        for (int k = 0; k < tempFieldArray.length; k++){
            if (tempFieldArray[k] == ','){
               ARRAY[arrayCounter] = tempArrayString;
               //System.out.println("This is the ARRAY number i am storing: " + tempArrayString);
               tempArrayString = "";
               arrayCounter++;
            } else {
                   tempArrayString = tempArrayString + tempFieldArray[k];
            }
        }

        return ARRAY;
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // if user selects to run C3PO

        //grab the for005 file from the text area
        for005 = jTextArea1.getText();

        //grab the MACH numbers from the text field
        String tempMACH = jTextField2.getText();
        MACH = getArrayFromString(tempMACH);
        
        //grab the ALT numbers from the text field
        String tempALT = jTextField1.getText();
        ALT = getArrayFromString(tempALT);

        //create a new instance of C3PO
        runMore = new C3PO(for005);

        //create a MASTER XML list
        String mkDir = "cmd /c mkdir " + workingPath + "\\" + caseID + "\\C3PO\\MASTER";
        runBabyRun.executeDatcom(mkDir);
        
        //Run DATCOM for each corresponding ALTITUDE and MACH number
        for (int i = 0; i < ALT.length; i++)
        {
            int position = 0;
            for(int j = 0; j < MACH.length; j++){

            String[] openC3PO = {workingPath + "\\Datcom.bat"};

            String mkDirMACH = "cmd /c mkdir " + workingPath + "\\" + caseID + "\\C3PO\\" +ALT[i] +"\\"+ MACH[j]+"_" + ALT[i];
            for005 = runMore.alterFile(MACH[j],ALT[i]);

            //alter the command string
            batchFile.writeFile( workingPath, workingPath + "\\" + caseID + "\\C3PO\\" +ALT[i] +"\\"+ MACH[j]+"_" + ALT[i], caseID);
            runBabyRun.executeDatcom(mkDirMACH);
            
            //write the for005 File into the folder
            printFile.writeFile(workingPath + "\\" + caseID + "\\C3PO\\"+ALT[i] +"\\"+ MACH[j]+"_" + ALT[i], "for005.dat", for005);

            //run DATCOM
            runBabyRun.executeDatcom(openC3PO);

            //grab the aero parameters
            runMore.grabParameters(workingPath + "\\" + caseID + "\\C3PO\\" +ALT[i] +"\\"+ MACH[j]+"_" + ALT[i]);
            runMore.createMaster(workingPath + "\\" + caseID + "\\C3PO\\" +ALT[i] +"\\"+ MACH[j]+"_" + ALT[i], MACH[j], ALT[i], position,workingPath + "\\" + caseID + "\\C3PO\\MASTER\\" );
            position++;
            }
            position = 0;
        }

        for005panel = this;
        reviewPanel = new REVIEW(workingPath + "\\" + caseID + "\\C3PO\\" + ALT[ALT.length-1] + "\\" + MACH[MACH.length-1] +
                "_" + ALT[ALT.length-1] + "\\for006.dat", for005panel, mainClass,"Aero Tables: " +
                workingPath + "\\" + caseID +
                "\\C3PO\\MASTER\\");
        reviewPanel.showFor006();

        //switch panels
        setVisible(false);
        mainClass.setComponent(reviewPanel);
        reviewPanel.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

